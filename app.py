# ØªØ­Ù…ÙŠÙ„ stopwords
nltk.download('stopwords')
stop_words = set(stopwords.words('english'))

# ØªØ­Ù…ÙŠÙ„ Ø§Ù„Ù…Ù„ÙØ§Øª Ø§Ù„Ù…Ø­ÙÙˆØ¸Ø©
with open("model.pkl", "rb") as f:
    model = pickle.load(f)

